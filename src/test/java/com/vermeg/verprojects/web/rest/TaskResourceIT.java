    private static final String DEFAULT_NAME = "AAAAAAAAAA";
    private static final String UPDATED_NAME = "BBBBBBBBBB";

    private static final String DEFAULT_DESCRIPTION = "AAAAAAAAAA";
    private static final String UPDATED_DESCRIPTION = "BBBBBBBBBB";

    private static final LocalDate DEFAULT_START_DATE = LocalDate.ofEpochDay(0L);
    private static final LocalDate UPDATED_START_DATE = LocalDate.now(ZoneId.systemDefault());

    private static final LocalDate DEFAULT_END_DATE = LocalDate.ofEpochDay(0L);
    private static final LocalDate UPDATED_END_DATE = LocalDate.now(ZoneId.systemDefault());

    private static final BigDecimal DEFAULT_ESTIMATED_COST = new BigDecimal(1);
    private static final BigDecimal UPDATED_ESTIMATED_COST = new BigDecimal(2);

    private static final TaskStatus DEFAULT_STATUS = TaskStatus.NOT_STARTED;
    private static final TaskStatus UPDATED_STATUS = TaskStatus.IN_PROGRESS;

